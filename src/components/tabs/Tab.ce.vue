<script>
export default {
    name: 'Tab',
    props: {
        name: {
            type: String,
            default: '',
        },
        id: {
            type: String,
            default: '',
        },
    },
    computed: {
        computedName() {
            return this.name || 'Таб';
        }
    }
};
</script>


<template>
    <div class="tab" :id="id">
        {{ computedName }}
        <slot></slot>
        <span class="underline"></span>
    </div>
</template>

<style scoped>
.tab {
    cursor: pointer;
    max-width: max-content;
    position: relative;
    border-radius: 5px;
    color: var(--secondary-text);
}

.tab:hover {
    transition: color .3s;
    color: var(--black-text);
}

.tab:hover .underline {
    width: 100%;
}

.underline {
    width: 0;
    height: 2px;
    background-color: var(--base-color);
    position: absolute;
    bottom: -12px;
    left: 0;
    transition: width .3s ease-in-out;
}
</style>